returns trigger as $$
declare
  is_first_user boolean;
  target_role_name text;
begin
  -- Check if this is the first user in the system
  select count(*) = 0 into is_first_user from public.profiles;
  
  if is_first_user or new.email = 'sleqcsleqc@gmail.com' then
    target_role_name := 'System Administrator';
  else
    target_role_name := 'Tenant';
  end if;

  insert into public.profiles (id, email, first_name, last_name, role_id)
  values (
    new.id, 
    new.email, 
    new.raw_user_meta_data->>'first_name', 
    new.raw_user_meta_data->>'last_name', 
    (select id from public.roles where name = target_role_name limit 1)
  );
  return new;
end;
$$ language plpgsql security definer;

