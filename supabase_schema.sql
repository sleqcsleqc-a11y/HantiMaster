-- Trigger to update tenant balance on transaction
create or replace function public.update_tenant_balance()
returns trigger as $$
begin
  if new.tenant_id is not null then
    if new.type = 'Charge' then
      update public.tenants 
      set current_balance = current_balance + new.amount 
      where id = new.tenant_id;
    elsif new.type = 'Payment' then
      update public.tenants 
      set current_balance = current_balance - new.amount 
      where id = new.tenant_id;
    elsif new.type = 'Refund' then
      update public.tenants 
      set current_balance = current_balance + new.amount 
      where id = new.tenant_id;
    end if;

    -- Special handling for Security Deposits
    if new.category = 'Security Deposit' then
       if new.type = 'Payment' then
         update public.tenants set security_deposit_held = security_deposit_held + new.amount where id = new.tenant_id;
       elsif new.type = 'Refund' then
         update public.tenants set security_deposit_held = security_deposit_held - new.amount where id = new.tenant_id;
       end if;
    end if;
  end if;
  return new;
end;
$$ language plpgsql security definer;

