-- Helper function to check admin status (bypasses RLS to avoid recursion)
create or replace function public.is_admin()
returns boolean as $$
begin
  return exists (
    select 1 from public.profiles
    join public.roles on profiles.role_id = roles.id
    where profiles.id = auth.uid() and roles.name in ('System Administrator', 'HR Manager', 'Property Manager', 'Finance Team')
  );
end;
$$ language plpgsql security definer;

